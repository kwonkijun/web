// 각 문장의 결과값을 예측해 보세요.

// 1. AND 연산
console.log(1 < 2 && 'Hello' !== 'Hello');
console.log(true && !false);

// 2. OR 연산
console.log(7 !== 7 || 4 > 3);
console.log(false || 4 < 2);

// 3. 변수와 결합
let x = 3;
console.log(x > 4 || !(x > 2));

// 4. 마무리
let x = 4;
let y = false;
let z = 'H';

console.log(x < 5 && 'Hello' === 'Hello');

console.log(y || !y);

console.log('Hello' !== z + 'ello');

console.log(x < 4 || !(y === true));







